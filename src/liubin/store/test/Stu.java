package liubin.store.test;

public class Stu {
	private String name;
	public Stu(String name, String age) {
		super();
		this.name = name;
		this.age = age;
	}
	private String age;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	
}
